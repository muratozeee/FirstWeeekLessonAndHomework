package kalýnSesliVeInceSesli;

public class kalýnSesliVeInceSesli {

	public static void main(String[] args) {
		char harf = 'ý';

		switch (harf) {

		case 'A':
		case 'I':
		case 'O':
		case 'U':
			System.out.println("Kalýn Sesli Harf");
			break;

		default:
			System.out.println("Ýnce Sesli Harf");

		}

	}
}
